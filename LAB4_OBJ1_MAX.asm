.DATA
COUNT DB 04H
VALUE DB 0FH,09H,14H,10H
RES DB ?

.CODE
MAIN PROC
    MOV AX,DATA
    MOV DS,AX
    MOV CL,COUNT
    DEC CL
    LEA SI,VALUE
    MOV AL,[SI]
    UP:INC SI
    CMP AL,[SI]
    JNC DWN
    MOV AL,[SI]
    DWN:DEC CL
    JNZ UP
    LEA DI,RES
    MOV [DI],AL
END MAIN